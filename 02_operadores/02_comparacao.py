'''
Esses operadores comparam valores e retornam True ou False.

    == (igual a): a == b
    != (diferente de): a != b
    > (maior que): a > b
    < (menor que): a < b
    >= (maior ou igual a): a >= b
    <= (menor ou igual a): a <= b
'''

a = 15
b = 0

print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)