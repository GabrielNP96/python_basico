"""Esses operadores são usados para operações matemáticas básicas.

    + (adição): a + b
    - (subtração): a - b
    * (multiplicação): a * b
    / (divisão): a / b (resultado é float)
    // (divisão inteira): a // b (resultado é inteiro)
    % (módulo): a % b (resto da divisão)
    ** (exponenciação): a ** b (a elevado a b)"""

a = 9
b = 2

print(a + b)
print(a - b)
print(a * b)
print(a / b)
